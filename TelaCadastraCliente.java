    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //Salvar 
        Cliente c = new Cliente();
        ClienteDAO cd = new ClienteDAO();
        c.setCodigo(Integer.parseInt(Codigotext.getText()));
        c.setNome(Nometext.getText());
        c.setSexo(String.valueOf(Sexocombo.getSelectedItem()));
        c.setIdade(Integer.parseInt(Idadetext.getText()));
        try {
            cd.SalvarCliente(c);
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadastraCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Excluir
        Cliente c = new Cliente();
        ClienteDAO cd = new ClienteDAO();
        int resposta = 0;
        resposta  = JOptionPane.showConfirmDialog(rootPane,"Deseja realmente excluir ?");
        if (resposta == JOptionPane.YES_OPTION){
            c.setCodigo(Integer.parseInt(Codigotext.getText()));
            try {
                cd.RemoverCliente(c);
            } catch (SQLException ex) {
                Logger.getLogger(TelaCadastraCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

